/**
 * @author Bootcamp User 005
 *this class has different methods which sets  and gets the values of a
 */
class IntegerWrapper {

	  private int a;
	  public IntegerWrapper(int a) { this.a = a; }
	  
	  
	  public int getA() {
		return a;
	}


	public void setA(int a) {
		this.a = a;
	}

	/**
	 * @param iw1
	 * @param iw2
	 * this method is used to swap the object values of  a to other object 
	 */
	public  static void  swap(IntegerWrapper iw1, IntegerWrapper iw2){
		int tmp;
		tmp=iw1.getA();
		iw1.setA(iw2.getA());
		iw2.setA(tmp);
		  
	  }
	}